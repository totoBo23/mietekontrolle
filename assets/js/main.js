function calculateResult() {
  const data = new FormData(form);
  const state = isInnsbruckSimplifiedFlow ? "Tirol" : data.get("state");
  const size = Number(data.get("apartment_size_m2")) || 0;
  const currentRent = Number(data.get("monthly_rent_net")) || 0;
  const buildingAge = data.get("building_age") || "unknown";
  const contractType = data.get("contract_type");
  const contractStartDate = parseLocalDate(data.get("contract_start_date"));
  const contractEndInput = parseLocalDate(data.get("contract_end_date"));
  const today = getTodayAtMidnight();
  const fullContractEndDate = contractType === "befristet" && contractEndInput ? contractEndInput : today;
  const soFarEndDate =
    contractType === "befristet" && contractEndInput && contractEndInput < today ? contractEndInput : today;
  const fullContractMonths = contractStartDate ? capRefundMonths(calculateContractMonths(contractStartDate, fullContractEndDate)) : 1;
  const soFarMonths = contractStartDate ? capRefundMonths(calculateContractMonths(contractStartDate, soFarEndDate)) : 1;

  const base = richtwertByState[state] || 0;
  const contractRentFactor = contractType === "befristet" ? FIXED_TERM_RENT_FACTOR : 1;

  const legalRentM2 = Math.max(base * contractRentFactor, 0);
  const legalRentTotal = legalRentM2 * size;
  const overpayment = Math.max(currentRent - legalRentTotal, 0);
  const refundSoFar = overpayment * soFarMonths;
  const fullContractOverpayment = contractType === "befristet" ? overpayment * fullContractMonths : null;
  const refund = refundSoFar;
  const futureAnnualOverpayment = contractType === "unbefristet" ? overpayment * 12 : null;

  const ratio = currentRent > 0 ? overpayment / currentRent : 0;

  let riskLevel = "low";
  if (overpayment > 250 || ratio >= 0.2) {
    riskLevel = "high";
  } else if (overpayment > 0) {
    riskLevel = "medium";
  }

  const qualified = riskLevel !== "low";

  lastResult = {
    legalRentM2,
    legalRentTotal,
    currentRent,
    overpayment,
    contractMonths: soFarMonths,
    soFarMonths,
    fullContractMonths,
    refund,
    refundSoFar,
    fullContractOverpayment,
    futureAnnualOverpayment,
    contractType,
    buildingAge,
    riskLevel,
    qualified,
    ineligibleReason: null
  };

  return lastResult;
}

function renderResult(result) {
  const ineligibleByAge = result.ineligibleReason === "building_age_from_1945_on";
  const lowReview = isLowOverpaymentReview(result);
  const isOpenEnded = result.contractType === "unbefristet";
  const isFixedTerm = result.contractType === "befristet";
  updateLeadBoxContent(result);
  const soFarDurationUnit = result.soFarMonths === 1 ? t("resultDurationUnitSingular") : t("resultDurationUnit");
  const fullDurationUnit =
    result.fullContractMonths === 1 ? t("resultDurationUnitSingular") : t("resultDurationUnit");

  if (resultUi.intro) {
    resultUi.intro.hidden = ineligibleByAge;
  }
  if (resultUi.ineligibleMessage) {
    resultUi.ineligibleMessage.hidden = !ineligibleByAge;
    resultUi.ineligibleMessage.textContent = t("resultIneligibleByAgeMessage");
  }
  if (resultUi.overview) {
    resultUi.overview.hidden = ineligibleByAge;
  }
  if (resultUi.details) {
    resultUi.details.hidden = ineligibleByAge;
  }
  resultFields.summary.hidden = ineligibleByAge;
  if (resultUi.disclaimer) {
    resultUi.disclaimer.hidden = ineligibleByAge;
  }

  if (ineligibleByAge) {
    resultFields.summary.textContent = "";
    if (resultUi.futureAnnualCard) {
      resultUi.futureAnnualCard.hidden = true;
    }
    qualifiedBox.hidden = false;
    notQualifiedBox.hidden = true;
    leadFeedback.textContent = "";

    [leadFields.name, leadFields.email, leadFields.phone, leadFields.consent].forEach((field) =>
      field?.removeAttribute("aria-invalid")
    );
    return;
  }

  resultFields.legalM2.textContent = Number.isFinite(result.legalRentM2)
    ? `${formatCurrency(result.legalRentM2)} ${t("resultPerSquareMeter")}`
    : "-";
  resultFields.legalTotal.textContent = Number.isFinite(result.legalRentTotal) ? formatCurrency(result.legalRentTotal) : "-";
  resultFields.currentRent.textContent = Number.isFinite(result.currentRent) ? formatCurrency(result.currentRent) : "-";
  resultFields.overpayment.textContent = Number.isFinite(result.overpayment) ? formatCurrency(result.overpayment) : "-";
  resultFields.refund.textContent = Number.isFinite(result.refundSoFar) ? formatCurrency(result.refundSoFar) : "-";
  resultFields.futureAnnual.textContent = Number.isFinite(isOpenEnded ? result.futureAnnualOverpayment : result.fullContractOverpayment)
    ? formatCurrency(isOpenEnded ? result.futureAnnualOverpayment : result.fullContractOverpayment)
    : "-";

  if (resultUi.refundLabel) {
    resultUi.refundLabel.textContent = `${t("resultRefundToDateLabel")} (${result.soFarMonths} ${soFarDurationUnit})`;
  }
  if (resultUi.futureAnnualLabel) {
    resultUi.futureAnnualLabel.textContent = isOpenEnded
      ? t("resultAnnualFutureLabel")
      : `${t("resultFullContractLabel")} (${result.fullContractMonths} ${fullDurationUnit})`;
  }
  if (resultUi.futureAnnualHelp) {
    resultUi.futureAnnualHelp.textContent = isOpenEnded ? t("resultAnnualFutureHelp") : t("resultFullContractHelp");
  }
  if (resultUi.futureAnnualCard) {
    resultUi.futureAnnualCard.hidden = !(isOpenEnded || isFixedTerm);
  }

  const summaryParts = [];
  if (result.riskLevel === "high") {
    summaryParts.push(t("summaryHigh"));
  } else if (result.riskLevel === "medium") {
    summaryParts.push(t("summaryMedium"));
  } else {
    summaryParts.push(t("summaryLow"));
  }

  resultFields.summary.textContent = summaryParts.join(" ");

  qualifiedBox.hidden = !(result.qualified || lowReview);
  notQualifiedBox.hidden = true;
  leadFeedback.textContent = "";

  [leadFields.name, leadFields.email, leadFields.phone, leadFields.consent].forEach((field) =>
    field?.removeAttribute("aria-invalid")
  );
}

function validateLead() {
  if (!canSubmitLead()) {
    return false;
  }

  let firstInvalid = null;

  const leadInputs = [leadFields.name, leadFields.email, leadFields.phone];

  leadInputs.forEach((field) => {
    field.removeAttribute("aria-invalid");
    if (!field.value.trim() || !field.checkValidity()) {
      field.setAttribute("aria-invalid", "true");
      if (!firstInvalid) {
        firstInvalid = field;
      }
    }
  });

  leadFields.consent.removeAttribute("aria-invalid");
  if (!leadFields.consent.checked) {
    leadFields.consent.setAttribute("aria-invalid", "true");
    if (!firstInvalid) {
      firstInvalid = leadFields.consent;
    }
  }

  if (firstInvalid) {
    firstInvalid.reportValidity();
    return false;
  }

  return true;
}

