function initializeHeroPhotos() {
  if (!heroPhotoNodes.length) {
    return Promise.resolve();
  }

  const loadChecks = heroPhotoNodes.map(
    (node) =>
      new Promise((resolve) => {
        if (node.complete) {
          resolve(node.naturalWidth > 0);
          return;
        }

        node.addEventListener("load", () => resolve(true), { once: true });
        node.addEventListener("error", () => resolve(false), { once: true });
      }).then((isValid) => ({ node, isValid }))
  );

  return Promise.all(loadChecks).then((results) => {
    results.forEach(({ node, isValid }) => {
      if (!isValid) {
        node.remove();
      }
    });

    heroPhotoNodes = results.filter(({ isValid }) => isValid).map(({ node }) => node);
    heroPhotoIndex = 0;

    if (heroPhotoTimerId) {
      window.clearInterval(heroPhotoTimerId);
      heroPhotoTimerId = null;
    }

    setHeroPhoto(heroPhotoIndex);
  });
}

