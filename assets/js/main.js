function validateStep(step) {
  const current = stepElements.find((el) => Number(el.dataset.step) === step);

  if (!current) {
    return true;
  }

  let firstInvalid = null;

  current.querySelectorAll("input, select").forEach((field) => {
    field.removeAttribute("aria-invalid");

    if (!field.checkValidity()) {
      field.setAttribute("aria-invalid", "true");
      if (!firstInvalid) {
        firstInvalid = field;
      }
    }
  });

  if (firstInvalid) {
    firstInvalid.reportValidity();
    return false;
  }

  if (step === STEP_RENT && Number(numberFields.size.value) <= 0) {
    numberFields.size.setAttribute("aria-invalid", "true");
    numberFields.size.focus();
    return false;
  }

  if (step === STEP_RENT && Number(numberFields.netRent.value) <= 0) {
    numberFields.netRent.setAttribute("aria-invalid", "true");
    numberFields.netRent.focus();
    return false;
  }

  if (step === STEP_RENT) {
    const startDate = parseLocalDate(contractFields.start.value);
    const endDate = parseLocalDate(contractFields.end.value);
    const contractType = form.querySelector('input[name="contract_type"]:checked')?.value;
    const today = getTodayAtMidnight();

    contractFields.start.setCustomValidity("");
    contractFields.end.setCustomValidity("");

    if (startDate && startDate > today) {
      contractFields.start.setAttribute("aria-invalid", "true");
      contractFields.start.setCustomValidity(t("contractDateFutureError"));
      contractFields.start.reportValidity();
      return false;
    }

    if (contractType === "befristet" && !contractFields.end.value) {
      contractFields.end.setAttribute("aria-invalid", "true");
      contractFields.end.setCustomValidity(t("contractEndRequiredError"));
      contractFields.end.reportValidity();
      return false;
    }

    if (contractType === "befristet" && startDate && endDate && endDate < startDate) {
      contractFields.end.setAttribute("aria-invalid", "true");
      contractFields.end.setCustomValidity(t("contractDateOrderError"));
      contractFields.end.reportValidity();
      return false;
    }
  }

  return true;
}

