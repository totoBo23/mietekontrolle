function parseCounterText(value) {
  const match = value.trim().match(/^(-?\d+(?:[.,]\d+)?)(.*)$/);
  if (!match) {
    return null;
  }

  const numeric = Number(match[1].replace(",", "."));
  if (Number.isNaN(numeric)) {
    return null;
  }

  return { target: numeric, suffix: match[2] };
}

function animateCounter(node) {
  const parsed = parseCounterText(node.textContent);
  if (!parsed) {
    return;
  }

  const { target, suffix } = parsed;
  const decimals = Number.isInteger(target) ? 0 : 1;
  const duration = 900;
  const start = performance.now();

  function frame(now) {
    const progress = Math.min((now - start) / duration, 1);
    const eased = 1 - Math.pow(1 - progress, 3);
    const currentValue = target * eased;
    const rounded = decimals === 0 ? Math.round(currentValue) : Number(currentValue.toFixed(decimals));

    node.textContent = `${rounded}${suffix}`;

    if (progress < 1) {
      requestAnimationFrame(frame);
    }
  }

  requestAnimationFrame(frame);
}

function animateHeroCounters() {
  if (prefersReducedMotion || heroCountersAnimated || !heroCounterNodes.length) {
    return;
  }

  heroCountersAnimated = true;
  heroCounterNodes.forEach((node, index) => {
    window.setTimeout(() => animateCounter(node), index * 120);
  });
}

function refreshHeroCounterAnimation() {
  if (!heroCounterNodes.length || prefersReducedMotion) {
    return;
  }

  heroCountersAnimated = false;
  const heroMetrics = document.querySelector(".hero-metrics");
  if (!heroMetrics) {
    return;
  }

  const rect = heroMetrics.getBoundingClientRect();
  const inView = rect.top < window.innerHeight * 0.88 && rect.bottom > 0;

  if (inView) {
    animateHeroCounters();
  } else if (heroCounterObserver) {
    heroCounterObserver.observe(heroMetrics);
  }
}

function initializeHeroCounterObserver() {
  if (prefersReducedMotion) {
    return;
  }

  const heroMetrics = document.querySelector(".hero-metrics");
  if (!heroMetrics) {
    return;
  }

  heroCounterObserver = new IntersectionObserver(
    (entries, observer) => {
      entries.forEach((entry) => {
        if (entry.isIntersecting) {
          animateHeroCounters();
          observer.unobserve(entry.target);
        }
      });
    },
    { threshold: 0.45 }
  );

  heroCounterObserver.observe(heroMetrics);
}

function initializeScrollReveal() {
  const targets = Array.from(
    document.querySelectorAll(
      "#why-we-do-this .didyouknow-eyebrow, #why-we-do-this h2, #why-we-do-this .section-lead, #why-we-do-this .why-points p, #why-we-do-this .didyouknow-media, #how-it-works .process-copy h2, #how-it-works .process-copy .section-lead, #how-it-works .process-main-cta, #how-it-works .process-item, #trust h2, #trust .section-lead, #trust .card, #trust .trust-panel, #calculator .section-header, #calculator .calculator-shell, #insights h2, #insights .section-lead, #insights .insight-card, #references h2, #references .section-lead, #references .reference-slider, #references .reference-dots, #faq h2, #faq details"
    )
  );

  if (!targets.length) {
    return;
  }

  targets.forEach((node, index) => {
    node.classList.add("reveal-item");
    node.style.setProperty("--reveal-delay", `${Math.min((index % 6) * 60, 300)}ms`);
  });

  if (prefersReducedMotion) {
    targets.forEach((node) => node.classList.add("is-revealed"));
    return;
  }

  const observer = new IntersectionObserver(
    (entries, revealObserver) => {
      entries.forEach((entry) => {
        if (entry.isIntersecting) {
          entry.target.classList.add("is-revealed");
          revealObserver.unobserve(entry.target);
        }
      });
    },
    { threshold: 0.14, rootMargin: "0px 0px -8% 0px" }
  );

  targets.forEach((node) => observer.observe(node));
}

function calculateContractMonths(startDate, endDate) {
  const msInDay = 1000 * 60 * 60 * 24;
  const dayDiff = Math.floor((endDate - startDate) / msInDay) + 1;
  return Math.max(1, Math.ceil(dayDiff / 30.4375));
}

function capRefundMonths(months) {
  return Math.min(months, REFUND_HORIZON_MONTHS);
}

